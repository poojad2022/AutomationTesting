package Day3.TabNewline;

public class Example1 {

	public static void main(String[] args) {
		System.out.println("Program Starts");
		System.out.println("My Name is:\tShailesh");
		System.out.println("You can contact me on:\t9764975157");
		System.out.println("#1003, Shivaji Nagar\nPune, Maharastra 411005");
		System.out.println("Program Ends");
	}
}
/*
Program Starts
My Name is:		Shailesh
You can contact me on:		9764975157
#1003, Shivaji Nagar
Pune, Maharastra 41105
Program ends

\t: pressing tab 
\n: new line, whenever you required
*/
